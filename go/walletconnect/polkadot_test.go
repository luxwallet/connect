// --- end-to-end via VerifyProof (the one entry point) ---

func TestPolkadotVerifyProofEndToEnd(t *testing.T) {
	res := VerifyProof(katSr25519.proof(), Expectation{
		Domain: "hanzo.id",
		Nonce:  "dotNonce0001",
		Now:    mustTime(t, "2023-11-14T22:14:00.000Z"),
	})
	if !res.OK {
		t.Fatalf("VerifyProof(sr25519 KAT) not OK: reason=%s", res.Reason)
	}
	if res.Chain != ChainPolkadot {
		t.Fatalf("chain = %q, want polkadot", res.Chain)
	}
	if res.Address != katSr25519.address {
		t.Fatalf("address = %q, want %q", res.Address, katSr25519.address)
	}
}

