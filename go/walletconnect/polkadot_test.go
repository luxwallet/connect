// Mirrors src/__tests__/polkadot.test.ts. Two kinds of coverage:
//
//  1. Cross-language KAT vectors: signatures produced by the TS side
//     (@polkadot/util-crypto, the same wallet a browser drives) over the
//     EXACT wrapped CAIP-122 message, which Go MUST verify byte-for-byte.
//     If the Go verifier ever drifts from the TS verifier, these fail.
//  2. In-Go round-trips (sr25519 via curve25519-voi, ed25519 via stdlib) plus
//     fail-closed hardening.
//
// sr25519 is verified with github.com/oasisprotocol/curve25519-voi
// (BSD-3-Clause) — NOT the LGPL ChainSafe/go-schnorrkel. ed25519 is stdlib;
// ecdsa is decred's secp256k1 (ISC). Zero copyleft.

