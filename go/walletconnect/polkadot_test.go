// --- In-Go round-trips (independent of the TS-minted KATs). ---

// substrateSS58Encode encodes a 32-byte AccountId to an SS58 address with the
// given 1-byte network prefix. Independent of the verifier's decodeSS58 (sign
// side vs verify side), so drift surfaces as a failing round-trip.
func substrateSS58Encode(t *testing.T, accountID []byte, prefix byte) string {
	t.Helper()
	body := append([]byte{prefix}, accountID...)
	preimage := append(append([]byte{}, ss58Pre...), body...)
	full := blake2b512(preimage)
	full2 := full[:ss58ChecksumLen]
	raw := append(append([]byte{}, body...), full2...)
	return base58EncodeBitcoin(raw)
}

// base58EncodeBitcoin is a tiny Bitcoin-alphabet base58 encoder for the test
// SS58 encoder (the verifier only decodes, via mr-tron/base58).
func base58EncodeBitcoin(b []byte) string {
	const alphabet = "123456789ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz"
	zeros := 0
	for zeros < len(b) && b[zeros] == 0 {
		zeros++
	}
	// base-256 -> base-58
	digits := []int{0}
	for i := zeros; i < len(b); i++ {
		carry := int(b[i])
		for j := 0; j < len(digits); j++ {
			carry += digits[j] << 8
			digits[j] = carry % 58
			carry /= 58
		}
		for carry > 0 {
			digits = append(digits, carry%58)
			carry /= 58
		}
	}
	out := make([]byte, 0, zeros+len(digits))
	for i := 0; i < zeros; i++ {
		out = append(out, '1')
	}
	for i := len(digits) - 1; i >= 0; i-- {
		out = append(out, alphabet[digits[i]])
	}
	return string(out)
}

