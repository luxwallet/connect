func TestPolkadotDoesNotPanicOnGarbage(t *testing.T) {
	garbage := Proof{
		Chain:     ChainPolkadot,
		Scheme:    SchemeSr25519,
		Address:   "!!!notss58!!!",
		PublicKey: "nothex",
		Message:   "not a siwx message",
		Signature: "!!!!",
	}
	defer func() {
		if r := recover(); r != nil {
			t.Fatalf("VerifyPolkadot panicked on garbage: %v", r)
		}
	}()
	if VerifyPolkadot(garbage) {
		t.Fatal("VerifyPolkadot(garbage) = true, want false")
	}
}

